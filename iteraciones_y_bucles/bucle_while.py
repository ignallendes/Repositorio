# Bucle 'while' ----> En español 'mientras'
# En programación un bucle while nos permite ejecutar un bloque de código siempre y cuando se cumpla una condición

# El siguiente codigo imprime por pantalla los números del 1 al 10
n = 1
while n <= 10: # El bucle se ejecuta miestras n <= 10
    print(n)
    n = n + 1  # Se incrementa el valor de n en una unidad por cada iteracion que se cumpla la condición

# La condición debe ser una expresión que retorne un valor booleano
# Iteración:
# Cada repetición de un bucle se denomina "iteración".
# En el codigo anterior se realizan 10 iteraciones, ya que se ejecutó 10 veces para mostrar los números requeridos