# Cuando  los programas comienzan a ser un poco complejos, es facil que comiencen a surguir errores
# y situaciones excepcionales
# Estas excepiones deben ser tratadas para tener un programa robusto
# ValueError
# precio = "Caro"
# precio_num = float(precio)
# Al ejecutar el codigo anterior se nos muestra por pantalla lo siguiente:
# ValueError: could not convert string to float: 'Caro'
# Esto ocurre ya que no se puede pasar una cadena de texto a un valor float
# ZeroDivisionError
# a = 17
# b = 0
# print(a / b)
# Al tratar de dividir un numero por cero se nos muestra el error ZeroDivisionError: division by zero
# NameError
# c = z + 2
# print(c)
# Al tratar de utilizar la variable z que no esta definida se produce el error:
# NameError: name 'z' is not defined

